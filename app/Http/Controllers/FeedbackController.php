    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validate = Validator::make(
            $request->all(),
            [ 
                'name' => 'required | unique:categories',
            ], 
            [ 
                'required' => ':attribute Do not leave blank',
            ] 
        );
        if($validate->fails()){
            return redirect()->back()->withErrors($validate);
        }
        else{
            $feeback = new Feedback;
            $feeback->name = $request->input('name');
            $feeback->email = $request->input('email');
            $feeback->subject = $request->input('subject');
            $feeback->content = $request->input('content');
            $feeback->save();
            session()->flash('msg', 'The new category was created successfully!!');
            return redirect()->back();
        }
    }

